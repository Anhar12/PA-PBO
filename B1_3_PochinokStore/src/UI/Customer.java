    private void display_jumlah(String game){
        int idx = Main.cari_game(game);
        
        cbJumlah.removeAllItems();
        cbJumlah.addItem(" ");
        if (idx != -1){
            for (int i = 0 ; i < Main.dataGame.get(idx).getVarian().size() ; i++){
                String jumlah = Main.dataGame.get(idx).getVarian().get(i).get(0).toString();
                String mataUang = Main.dataGame.get(idx).getMataUang();
                cbJumlah.addItem(jumlah + " " + mataUang);
            }
        }
    }
    
