    private void btnTopUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTopUpActionPerformed
        if (cek() == true){
            int pilih = JOptionPane.showConfirmDialog(null, "Yakin Data Akun Anda Sudah Benar?", "Meyakinkan", JOptionPane.YES_NO_OPTION);
            if (pilih == JOptionPane.YES_OPTION){
                String game = cbGame.getSelectedItem().toString();
                String id_akun = txtID.getText();
                String nickname = txtNickname.getText();
                int idx_game = Main.cari_game(game);
                String mataUang = Main.dataGame.get(idx_game).getMataUang();
                int idx_topup = cbJumlah.getSelectedIndex() - 1;
                int jumlah = Main.dataGame.get(idx_game).getVarian().get(idx_topup).get(0);
                int harga = Main.dataGame.get(idx_game).getVarian().get(idx_topup).get(1);
                LocalDateTime timeNow = LocalDateTime.now();
                DateTimeFormatter waktu = DateTimeFormatter.ofPattern("E, MMM dd yyyy HH:mm:ss");
                String waktuSekarang = timeNow.format(waktu);
                Pesanan newData = new Pesanan(jumlah, harga, game, id_akun, nickname, waktuSekarang, mataUang);

                Main.dataPesanan.add(newData);
                MyDatabase.insert_transaksi(game, jumlah, harga, id_akun, nickname, waktuSekarang);
                Main.dataPesanan.get(Main.dataPesanan.size()-1).action();
            }
        }
    }//GEN-LAST:event_btnTopUpActionPerformed

