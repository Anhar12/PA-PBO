    @Override
    public void display(String game){
        int idx = Main.cari_game(game);
        
//        atur kolom header
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Jumlah");
        model.addColumn("Harga");
        
//        kalo misalnya data ketemu, maka akan diambil data dari arraylistnya ke tabel
        if (idx != -1){
            for (int i = 0 ; i < Main.dataGame.get(idx).getVarian().size() ; i++){
                String jumlah = Main.dataGame.get(idx).getVarian().get(i).get(0).toString() + " " + Main.dataGame.get(idx).getMataUang();
                String harga = "Rp" + Main.dataGame.get(idx).getVarian().get(i).get(1);
                model.addRow(new Object[] {(i+1), jumlah, harga});
            }
        }
        
//        set model dan atur lebar kolom
        TableOrder.setModel(model);
        TableOrder.getColumnModel().getColumn(0).setPreferredWidth(1);
        TableOrder.getColumnModel().getColumn(1).setPreferredWidth(100);
        TableOrder.getColumnModel().getColumn(2).setPreferredWidth(100);
        
//        align nomor to center
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        TableColumnModel columnModel = TableOrder.getColumnModel();
        for (int i = 0 ; i < 3 ;i++){
            columnModel.getColumn(i).setCellRenderer(centerRenderer);
        }
        TableOrder.setDefaultEditor(Object.class, null);
    }
    
    private boolean cek(){
        if (cbGame.getSelectedIndex() < 1){
            JOptionPane.showMessageDialog(this, "Pilih Game Yang Mau Di Top Up Dulu Masbro!", "Sekedar Mengingatkan", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (txtID.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "ID Akunnya Tidak Boleh Kosong Masbro!", "Sekedar Mengingatkan", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (txtNickname.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Nickname Akunnya Tidak Boleh Kosong Masbro!", "Sekedar Mengingatkan", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (cbJumlah.getSelectedIndex() < 1){
            JOptionPane.showMessageDialog(this, "Pilih Jumlah Top Upnya Dulu Masbro!", "Sekedar Mengingatkan", JOptionPane.WARNING_MESSAGE);
            return false;
        }
            
        return true;
    }
    
